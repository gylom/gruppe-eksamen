export const LANGUAGE_STORAGE_KEY = "chopchopshop-language"

function readStoredLanguage(): "nb" | "en" {
  if (typeof window === "undefined") return "nb"
  try {
    const raw = window.localStorage.getItem(LANGUAGE_STORAGE_KEY)
    if (raw === "en" || raw === "nb") return raw
  } catch {
    /* ignore */
  }
  return "nb"
}

function persistLanguage(lng: string) {
  if (typeof window === "undefined") return
  try {
    if (lng === "en" || lng === "nb") {
      window.localStorage.setItem(LANGUAGE_STORAGE_KEY, lng)
    }
  } catch {
    /* ignore */
  }
}

function syncDocumentLang(lng: string) {
  if (typeof document === "undefined") return
  document.documentElement.lang = lng === "en" ? "en" : "nb"
}

